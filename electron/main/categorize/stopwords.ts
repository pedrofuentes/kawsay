// Hand-rolled trilingual (ES / EN / PT) stopword lists for theme-label derivation
// (ADR-0030 Decision 3, milestone M4-2, card M4-2f). Deliberately NOT an npm
// dependency (`stopword`, `natural`): a new NLP dep would flip this card to
// ⚠️ ask-first, so — in the ADR-0014/0015 "hand-roll it" ethos — we bundle a
// small, pure DATA module instead. It has no logic: `theme-labels.ts` folds these
// entries through its own tokenizer so the stopword set is normalized IDENTICALLY
// to the tokens it filters (lowercase + NFD diacritic strip), which is why entries
// are written in their NATURAL spelling here (e.g. "más", "não", "está") rather
// than pre-folded — folding is the single source of truth in one place.
//
// Scope: common function words (articles, pronouns, prepositions, conjunctions,
// auxiliaries, determiners) — the high-frequency, low-salience tokens that would
// otherwise dominate a TF/DF ranking. It is intentionally SMALL, not exhaustive;
// distinctive content words (place, event, and object nouns) must survive.

